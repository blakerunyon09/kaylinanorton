import { writeFile } from "node:fs/promises";

const source = process.env.WORDPRESS_SOURCE ?? "https://kaylinanorton.com";
const origin = process.env.WORDPRESS_ORIGIN ?? "https://wp.kaylinanorton.com";
const outputFile = new URL("../public/_redirects", import.meta.url);

const normalizeUrl = (value) => value.replace(/\/+$/, "");

const fetchJson = async (url) => {
  const response = await fetch(url);

  if (!response.ok) {
    throw new Error(`Failed to fetch ${url}: ${response.status}`);
  }

  return {
    data: await response.json(),
    totalPages: Number(response.headers.get("x-wp-totalpages") ?? 1),
  };
};

const fetchAllPosts = async () => {
  const endpoint = new URL("/wp-json/wp/v2/posts", normalizeUrl(source));
  endpoint.searchParams.set("per_page", "100");
  endpoint.searchParams.set("_fields", "slug,link");

  const firstPageUrl = new URL(endpoint);
  firstPageUrl.searchParams.set("page", "1");

  const firstPage = await fetchJson(firstPageUrl);
  const posts = [...firstPage.data];

  for (let page = 2; page <= firstPage.totalPages; page += 1) {
    const pageUrl = new URL(endpoint);
    pageUrl.searchParams.set("page", String(page));

    const { data } = await fetchJson(pageUrl);
    posts.push(...data);
  }

  return posts
    .filter((post) => typeof post.slug === "string" && post.slug.length > 0)
    .sort((a, b) => a.slug.localeCompare(b.slug));
};

const buildRedirects = (posts) => {
  const cleanOrigin = normalizeUrl(origin);
  const lines = [
    "# Generated by scripts/generate-wordpress-redirects.mjs",
    `# WordPress origin: ${cleanOrigin}`,
    `# WordPress source: ${normalizeUrl(source)}`,
    `# WordPress post count: ${posts.length}`,
    "",
    "# WordPress blog index, archives, media, feeds, and API",
    `/blog ${cleanOrigin}/blog/ 200!`,
    `/blog/ ${cleanOrigin}/blog/ 200!`,
    `/blog/* ${cleanOrigin}/blog/:splat 200!`,
    `/category/* ${cleanOrigin}/category/:splat 200!`,
    `/tag/* ${cleanOrigin}/tag/:splat 200!`,
    `/author/* ${cleanOrigin}/author/:splat 200!`,
    `/wp-content/uploads/* ${cleanOrigin}/wp-content/uploads/:splat 200!`,
    `/wp-json/* ${cleanOrigin}/wp-json/:splat 200!`,
    `/feed/ ${cleanOrigin}/feed/ 200!`,
    `/comments/feed/ ${cleanOrigin}/comments/feed/ 200!`,
    "",
    "# Root-level WordPress post URLs",
  ];

  for (const post of posts) {
    lines.push(`/${post.slug} ${cleanOrigin}/${post.slug}/ 200!`);
    lines.push(`/${post.slug}/ ${cleanOrigin}/${post.slug}/ 200!`);
  }

  lines.push("");

  return lines.join("\n");
};

const posts = await fetchAllPosts();
await writeFile(outputFile, buildRedirects(posts));

console.log(`Wrote ${outputFile.pathname} with ${posts.length} post rewrites.`);
